public class switch_statement {

    
    public static void main(String[] args) {
    //if a equals 10 modulus a by 2 and print result to console
    int a =10;
    switch(a){
        case 1:
            System.out.println(a+5);
            break;
        case 2:
            System.out.println(a+15);
            break;
        case 10:
            System.out.println(a%2);
            break;
    }
    }
}
