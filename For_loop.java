public class For_loop {

    public static void main(String[] args) {
        
        int num;
        System.out.println("Number          Number Squared");
        System.out.println("------------------------------------");
        for(num=1; num<=10;num++){
            System.out.println(num + "\t\t\t" + num*num);
        }
        
        
        
        
        
    }
}
